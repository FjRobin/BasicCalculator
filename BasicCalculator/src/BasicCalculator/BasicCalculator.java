    private void btnRSActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRSActionPerformed

        secnum = Double.parseDouble(tbxALL.getText());

        if (null != opa)switch (opa) {
            case "+":
            res = firstnum + secnum ;
            tbxALL.setText(String.valueOf(res));
            break;
            case "-":
            res = firstnum - secnum ;
            tbxALL.setText(String.valueOf(res));
            break;
            case "*":
            res = firstnum * secnum ;
            tbxALL.setText(String.valueOf(res));
            break;
            case "/":
            res = firstnum / secnum ;
            tbxALL.setText(String.valueOf(res));
            break;

            default:
            break;
        }
    }//GEN-LAST:event_btnRSActionPerformed

